package class06;

public class E6SwitchStatemen {
    public static void main(String[] args) {

        String name = "Vlad";
        switch (name) {
            case "Vlad":
                System.out.println("Greenfield");
                break;
            case "Jacob":
                System.out.println("Chicago");
                break;
            case "Axel":
                System.out.println("Inman");
        }

    }
}
